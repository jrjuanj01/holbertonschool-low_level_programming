#include <stdio.h>
#include <stdlib.h>
#include "3-calc.h"
/**
 * op_add - adds the two integers
 * @a: first int
 * @b: second int
 * Return: int
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtracts the two integers
 * @a: first int
 * @b: second int
 * Return: int
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplies the two integers
 * @a: first int
 * @b: second int
 * Return: int
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divides the two integers
 * @a: first int
 * @b: second int
 * Return: int
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - finds the modulous of the two integers
 * @a: first int
 * @b: second int
 * Return: int
 */
int op_mod(int a, int b)
{
	return (a % b);
}
